    {
        $label = strtolower($item['label']);
        $url = $item['url'] ?? '';

        $iconSvg = self::getIcon($label, $url === '' ? 'text-gray-400' : 'text-gray-900');

        $linkOrText = $url
            ? '<a href="' . $url . '" class="ml-2 text-sm font-medium hover:text-gray-900">' . $item['label'] . '</a>'
            : '<span class="ml-2 text-sm font-medium text-gray-400">' . $item['label'] . '</span>';

        $separator = !$isLast
            ? '<svg class="w-4 h-4 mx-2 text-gray-400" fill="currentColor" viewBox="0 0 20 20">
                    <path fill-rule="evenodd" d="M7.293 14.707a1 1 0 010-1.414L10.586 10 7.293 6.707a1 1 0 011.414-1.414l4 4a1 1 0 010 1.414l-4 4a1 1 0 01-1.414 0z" clip-rule="evenodd" />
               </svg>'
            : '';

        return '<li class="inline-flex items-center">'
                . $iconSvg
                . $linkOrText
                . $separator
            . '</li>';
    }

